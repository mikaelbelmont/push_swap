#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*cpy;

	i = 0;
	cpy = (char *)malloc((ft_strlen(str) + 1) * sizeof(char));
	if (cpy == 0)
		return (0);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
